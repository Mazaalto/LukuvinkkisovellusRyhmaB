package lukuvinkkisovellus;

public class Lukuvinkki {

    String otsikko;
    String url;

    public Lukuvinkki(String otsikko, String url) {
        this.otsikko = otsikko;
        this.url = url;
    }

    public String getOtsikko() {
        return otsikko;
    }

    public String getUrl() {
        return url;
    }
    
    

}
